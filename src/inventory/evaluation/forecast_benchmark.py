"""Forecast benchmark harness (didactic, lightweight).

Goal
----
Compare different *forecasters* on the same synthetic demand paths generated by an
*exogenous demand model*.

This supports the "lego blocks" workflow:
- Demand model: ground-truth simulator of D_{t+1} (and possibly latent/observable regime).
- Adapter: creates features + synthetic supervised training data from the demand model.
- Forecaster: learns/predicts a mean demand path.

Usage (repo root)
-----------------
Seasonal:
    poetry run python -m inventory.evaluation.forecast_benchmark --problem seasonal

Regime:
    poetry run python -m inventory.evaluation.forecast_benchmark --problem regime

Notes
-----
- Evaluation is fair: demand paths are simulated once (CRN-style) and reused across all forecasters.
- By convention, `forecast_mean_path(state, t, H)[0]` predicts E[D_{t+1}].
"""

from __future__ import annotations

import argparse
from dataclasses import dataclass
from typing import Dict, List, Optional, Sequence, Tuple

import numpy as np
from inventory.core.types import Action, State
from inventory.forecasters.base import DemandForecaster
from inventory.forecasters.factory import FitConfig, fit_ml_forecaster_from_exogenous
from inventory.forecasters.naive import ConstantMeanForecaster, ExogenousAwareMeanForecaster
from inventory.problems.demand_models import PoissonRegimeDemand, PoissonSeasonalDemand


@dataclass(frozen=True)
class BenchmarkConfig:
    T: int = 80
    H: int = 1
    n_paths: int = 30
    seed0: int = 2026
    inventory_level: float = 300.0
    r0: int = 0


def _simulate_demand_path(
    exo: object,
    *,
    T: int,
    seed: int,
    inventory_level: float,
    r0: int,
) -> Tuple[np.ndarray, Optional[np.ndarray]]:
    """Simulate one demand path of length T.

    Returns:
      demands: shape (T,) where demands[t] is D_{t+1}
      regimes: shape (T+1,) of R_t values if regime model, else None
    """

    rng = np.random.default_rng(int(seed))
    T = int(T)
    inv = float(inventory_level)

    dummy_action: Action = np.array([0.0], dtype=float)

    if isinstance(exo, PoissonRegimeDemand):
        regimes = np.empty(T + 1, dtype=int)
        regimes[0] = int(r0)
        demands = np.empty(T, dtype=float)

        for t in range(T):
            S: State = np.array([inv, float(regimes[t])], dtype=float)
            w = exo.sample(S, dummy_action, t, rng)
            demands[t] = float(w[0])
            regimes[t + 1] = int(np.round(float(w[1])))

        return demands, regimes

    if isinstance(exo, PoissonSeasonalDemand):
        demands = np.empty(T, dtype=float)
        S: State = np.array([inv], dtype=float)
        for t in range(T):
            w = exo.sample(S, dummy_action, t, rng)
            demands[t] = float(w[0])
        return demands, None

    raise NotImplementedError("Unsupported exogenous model for benchmark.")


def _default_state(exo: object, *, inventory_level: float, regime: int) -> State:
    if isinstance(exo, PoissonRegimeDemand):
        return np.array([float(inventory_level), float(regime)], dtype=float)
    return np.array([float(inventory_level)], dtype=float)


def _score_forecaster_on_paths(
    forecaster: DemandForecaster,
    exo: object,
    *,
    cfg: BenchmarkConfig,
    paths: Sequence[Tuple[np.ndarray, Optional[np.ndarray]]],
) -> Dict[str, float]:
    """Compute MAE/RMSE/Bias over 1..H steps ahead (averaged)."""

    H = int(cfg.H)
    T = int(cfg.T)

    abs_err: List[float] = []
    sq_err: List[float] = []
    bias: List[float] = []

    for demands, regimes in paths:
        for t in range(T):
            r_t = int(regimes[t]) if regimes is not None else int(cfg.r0)
            S = _default_state(exo, inventory_level=cfg.inventory_level, regime=r_t)
            mu = np.asarray(forecaster.forecast_mean_path(S, t, H, info={}), dtype=float).reshape(-1)
            h_eff = min(H, T - t)
            if mu.shape[0] < h_eff:
                raise ValueError(f"Forecaster returned mu of length {mu.shape[0]} but need {h_eff}.")

            y = np.asarray(demands[t : t + h_eff], dtype=float)
            e = mu[:h_eff] - y
            abs_err.extend(np.abs(e).tolist())
            sq_err.extend((e**2).tolist())
            bias.extend(e.tolist())

    abs_err_a = float(np.mean(abs_err)) if abs_err else float("nan")
    rmse_a = float(np.sqrt(np.mean(sq_err))) if sq_err else float("nan")
    bias_a = float(np.mean(bias)) if bias else float("nan")

    return {"MAE": abs_err_a, "RMSE": rmse_a, "Bias": bias_a}


def _make_problem(problem: str) -> object:
    if problem == "seasonal":
        return PoissonSeasonalDemand(base=300.0, amp=90.0, period=20, spike_every=50, spike_add=120.0)

    if problem == "regime":
        P = np.array([[0.92, 0.08], [0.18, 0.82]], dtype=float)
        return PoissonRegimeDemand(lam_by_regime=[220.0, 460.0], P=P, regime_index=1)

    raise ValueError("problem must be one of: seasonal, regime")


def _registry(
    exo: object,
    *,
    random_state: int,
    fit_cfg: FitConfig,
) -> Dict[str, DemandForecaster]:
    """Build the full set of forecasters that can be benchmarked.

    The caller can then select a subset via CLI.
    """

    out: Dict[str, DemandForecaster] = {}

    out["const_350"] = ConstantMeanForecaster(mean=350.0)
    out["oracle_exogenous"] = ExogenousAwareMeanForecaster(exogenous_model=exo)

    # ML backends (fit each on synthetic data from the exogenous model)
    for model_type in ("linear", "tree", "mlp"):
        ml, _rep = fit_ml_forecaster_from_exogenous(
            exo,
            model_type=model_type,
            random_state=random_state,
            fit=fit_cfg,
        )
        out[f"ml_{model_type}"] = ml

    return out


def available_forecaster_names() -> List[str]:
    """Names that `--forecasters` can accept."""

    return ["const_350", "oracle_exogenous", "ml_linear", "ml_tree", "ml_mlp"]


def _select_forecasters(all_forecasters: Dict[str, DemandForecaster], wanted: Optional[Sequence[str]]) -> Dict[str, DemandForecaster]:
    if not wanted:
        return all_forecasters

    selected: Dict[str, DemandForecaster] = {}
    missing: List[str] = []
    for name in wanted:
        key = str(name).strip()
        if not key:
            continue
        if key not in all_forecasters:
            missing.append(key)
            continue
        selected[key] = all_forecasters[key]

    if missing:
        avail = ", ".join(sorted(all_forecasters.keys()))
        raise SystemExit(f"Unknown forecaster(s): {missing}. Available: {avail}")

    return selected


def _print_table(rows: List[Tuple[str, Dict[str, float]]]) -> None:
    headers = ["name", "MAE", "RMSE", "Bias"]
    data = [(name, m["MAE"], m["RMSE"], m["Bias"]) for name, m in rows]

    col_w = {
        "name": max(len(headers[0]), *(len(r[0]) for r in data)),
        "MAE": len("MAE"),
        "RMSE": len("RMSE"),
        "Bias": len("Bias"),
    }

    def fmt_f(x: float) -> str:
        if not np.isfinite(x):
            return "nan"
        return f"{x:8.2f}".strip()

    lines: List[str] = []
    lines.append(
        f"{headers[0]:<{col_w['name']}}  {headers[1]:>8}  {headers[2]:>8}  {headers[3]:>8}"
    )
    lines.append("-" * (col_w["name"] + 2 + 8 + 2 + 8 + 2 + 8))

    for name, mae, rmse, bias in data:
        lines.append(
            f"{name:<{col_w['name']}}  {fmt_f(mae):>8}  {fmt_f(rmse):>8}  {fmt_f(bias):>8}"
        )

    print("\n".join(lines))


def main(argv: Optional[Sequence[str]] = None) -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--problem", choices=["seasonal", "regime"], default="seasonal")
    ap.add_argument("--T", type=int, default=80)
    ap.add_argument("--H", type=int, default=1, help="steps ahead; aggregates errors over 1..H")
    ap.add_argument("--n-paths", type=int, default=30)
    ap.add_argument("--seed0", type=int, default=2026)
    ap.add_argument("--random-state", type=int, default=0)
    ap.add_argument("--train-samples", type=int, default=800)
    ap.add_argument("--val-samples", type=int, default=200)
    ap.add_argument("--r0", type=int, default=0, help="initial regime for simulated paths (regime problem)")
    ap.add_argument(
        "--forecasters",
        nargs="*",
        default=None,
        help="Explicit list of forecasters to benchmark (default: all). "
        "Examples: --forecasters const_350 oracle_exogenous ml_linear ml_tree",
    )
    ap.add_argument(
        "--list-forecasters",
        action="store_true",
        help="Print available forecaster names and exit.",
    )

    args = ap.parse_args(list(argv) if argv is not None else None)

    if bool(args.list_forecasters):
        print("\n".join(available_forecaster_names()))
        return 0

    exo = _make_problem(str(args.problem))
    cfg = BenchmarkConfig(T=int(args.T), H=int(args.H), n_paths=int(args.n_paths), seed0=int(args.seed0), r0=int(args.r0))

    # Simulate paths once (fair evaluation).
    paths = [
        _simulate_demand_path(
            exo,
            T=cfg.T,
            seed=cfg.seed0 + i,
            inventory_level=cfg.inventory_level,
            r0=cfg.r0,
        )
        for i in range(cfg.n_paths)
    ]

    fit_cfg = FitConfig(n_samples=int(args.train_samples), seed=11, val_samples=int(args.val_samples), val_seed=22)
    all_forecasters = _registry(exo, random_state=int(args.random_state), fit_cfg=fit_cfg)
    forecasters = _select_forecasters(all_forecasters, args.forecasters)

    rows: List[Tuple[str, Dict[str, float]]] = []
    for name, f in forecasters.items():
        m = _score_forecaster_on_paths(f, exo, cfg=cfg, paths=paths)
        rows.append((name, m))

    # sort by RMSE
    rows.sort(key=lambda kv: kv[1]["RMSE"])

    sel = ",".join(list(forecasters.keys()))
    print(f"Problem: {args.problem} | paths={cfg.n_paths} | T={cfg.T} | H={cfg.H} | forecasters={sel}")
    _print_table(rows)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
